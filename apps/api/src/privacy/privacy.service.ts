@Injectable()
export class PrivacyService {
  constructor(@Inject('PrismaClient') private prisma: PrismaClient) {}

  async getCurrent(): Promise<PrivacyTerms> {
    const row = await this.prisma.siteContent.findUnique({ where: { key: PRIVACY_KEY } });
    if (row && row.value && typeof row.value === 'object') {
      return row.value as unknown as PrivacyTerms;
    }
    return DEFAULT_PRIVACY;
  }

  async updateCurrent(data: { title?: string; body?: string; version?: string }, updatedBy: string) {
    const current = await this.getCurrent();
    const next: PrivacyTerms = {
      version: data.version || new Date().toISOString().slice(0, 10),
      title: data.title ?? current.title,
      body: data.body ?? current.body,
      updatedAt: new Date().toISOString(),
    };

    await this.prisma.siteContent.upsert({
      where: { key: PRIVACY_KEY },
      update: { value: next as any, updatedBy },
      create: { key: PRIVACY_KEY, value: next as any, updatedBy },
    });

    return next;
  }

  async getMyAcceptance(brokerId: string) {
    const privacy = await this.getCurrent();
    const acceptance = await this.prisma.privacyAcceptance.findFirst({
      where: { brokerId, privacyVersion: privacy.version },
      orderBy: { acceptedAt: 'desc' },
    });

    return {
      privacy,
      accepted: !!acceptance,
      acceptance: acceptance
        ? {
            id: acceptance.id,
            privacyVersion: acceptance.privacyVersion,
            acceptedAt: acceptance.acceptedAt,
          }
        : null,
    };
  }

  async accept(brokerId: string, ip: string | null, userAgent: string | null) {
    const privacy = await this.getCurrent();

    const existing = await this.prisma.privacyAcceptance.findFirst({
      where: { brokerId, privacyVersion: privacy.version },
    });
    if (existing) {
      return { message: 'Согласие уже принято', acceptance: existing };
    }

    const created = await this.prisma.privacyAcceptance.create({
      data: { brokerId, privacyVersion: privacy.version, ip, userAgent },
    });

    await this.prisma.auditLog.create({
      data: {
        userId: brokerId,
        action: 'PRIVACY_ACCEPTED',
        entity: 'PrivacyAcceptance',
        entityId: created.id,
        payload: { privacyVersion: privacy.version },
        ip,
      },
    });

    return { message: 'Согласие на обработку ПД принято', acceptance: created };
  }
}
